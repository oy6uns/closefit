import UIKit

extension UIColor {
    @nonobjc class var mint: UIColor {
        return UIColor(red: 97.0 / 255.0, green: 203.0 / 255.0, blue: 209.0 / 255.0, alpha: 1)
    }
    
    @nonobjc class var blueWhite: UIColor {
        return UIColor(red: 238.0 / 255.0, green: 246.0 / 255.0, blue: 254.0 / 255.0, alpha: 1)
    }
    
    @nonobjc class var green: UIColor {
        return UIColor(red: 126.0 / 255.0, green: 193.0 / 255.0, blue: 174.0 / 255.0, alpha: 1)
    }
    
    @nonobjc class var black: UIColor {
        return UIColor(red: 0.0 / 255.0, green: 0.0 / 255.0, blue: 0.0 / 255.0, alpha: 1)
    }
    
    @nonobjc class var darkGray: UIColor {
        return UIColor(red: 74.0 / 255.0, green: 74.0 / 255.0, blue: 74.0 / 255.0, alpha: 1)
    }
    
    @nonobjc class var gray: UIColor {
